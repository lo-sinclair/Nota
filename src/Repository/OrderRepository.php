	public function sumOfOrder($orderId): int {
		$dql = "SELECT SUM(p.price) 
			FROM App\Entity\Order o
			JOIN o.products p
			WHERE o.id = :id
			GROUP BY o.id 
			";

		$query = $this->getEntityManager()->createQuery($dql);
		$query->setParameter('id', $orderId);
		return $query->getSingleResult()[1];
	}

